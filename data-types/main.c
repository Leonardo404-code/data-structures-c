#include <stdio.h>
#include <stdlib.h>

int a = 5;
float num = 2.3;
double d = 4.6;
char ch = 't';

int main()
{
  printf("o valor da variavel 'a' é: %d\n", a);
  printf("o valor da variavel 'num' é: %.1f\n", num);
  printf("o valor da variavel 'd' é: %f\n", d);
  printf("o valor da variavel 'ch' é: %c\n", ch);

  return 0;
}
